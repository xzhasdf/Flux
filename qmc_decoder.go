func qmcGetMask(idx int) byte {
	if idx > 0x7FFF {
		idx %= 0x7FFF
	}
	pos := (idx*idx + 71214) % 128
	return qmcStaticKey[pos]
}

// 根据原扩展名推测解码后的音频格式
var qmcFormatMap = map[string]string{
	".qmc0":    "mp3",
	".qmc3":    "mp3",
	".qmcflac": "flac",
	".qmcogg":  "ogg",
	".qmcra":   "ra",
	".bkcmp3":  "mp3",
	".bkcflac": "flac",
	".tkm":     "m4a",
}

func isQMCFile(path string) bool {
	ext := strings.ToLower(filepath.Ext(path))
	_, ok := qmcFormatMap[ext]
	return ok
}

func decodeQMCToTemp(src string) (*decodeResult, error) {
	ext := strings.ToLower(filepath.Ext(src))

	// .mflac / .mgg 等需要 EKEY + RC4 的格式暂不支持
	if ext == ".mflac" || ext == ".mgg" || ext == ".mflac0" || ext == ".mgg0" || ext == ".mgg1" {
		return nil, errors.New("暂不支持 .mflac/.mgg（需要 EKEY 解析）")
	}

	format, ok := qmcFormatMap[ext]
	if !ok {
		return nil, fmt.Errorf("不支持的 QMC 格式: %s", ext)
	}

	data, err := os.ReadFile(src)
	if err != nil {
		return nil, err
	}
	if len(data) == 0 {
		return nil, errors.New("QMC 文件为空")
	}

	decoded := make([]byte, len(data))
	for i, b := range data {
		decoded[i] = b ^ qmcGetMask(i)
	}

	tmpFile, err := os.CreateTemp("", fmt.Sprintf("qmc_decoded_*.%s", format))
	if err != nil {
		return nil, fmt.Errorf("创建临时文件失败: %w", err)
	}
	if _, err := tmpFile.Write(decoded); err != nil {
		tmpFile.Close()
		os.Remove(tmpFile.Name())
		return nil, fmt.Errorf("写入临时文件失败: %w", err)
	}
	tmpFile.Close()

	return &decodeResult{Path: tmpFile.Name(), Format: format}, nil
}
